const finalText = "BADCFEHGJILKNMPORQTSVUXWZY";
let completed = false;

function shuffle(array) {
    let currentIndex = array.length,
        randomIndex;

    while (currentIndex != 0) {
        randomIndex = Math.floor(Math.random() * currentIndex);
        currentIndex--;

        [array[currentIndex], array[randomIndex]] = [
            array[randomIndex],
            array[currentIndex],
        ];
    }

    return array;
}

while (!completed) {
    shuffle(alphabet);
    var text  = alphabet.join("");
    console.log(text);
    if (text == finalText) {
        console.log("Completed!");
        completed = true;
    }
}
